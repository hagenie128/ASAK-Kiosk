def make_text(pid: str, props: dict, mermaid: str | None) -> str:
    body = (
        f'<page url="https://app.notion.com/p/{pid}">\n'
        f"<properties>\n{json.dumps(props, ensure_ascii=False)}\n</properties>\n"
    )
    if mermaid:
        body += f"<content>\n```mermaid\n{mermaid}\n```\n</content>\n"
    body += "</page>"
    return body


