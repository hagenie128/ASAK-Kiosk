def save_page(page_id: str, title: str, props: dict) -> None:
    url = f"https://app.notion.com/p/{page_id}"
    text = (
        f'Here is the result of "view" for the Page with URL {url}.\n'
        f'<page url="{url}">\n<properties>\n'
        f"{json.dumps(props, ensure_ascii=False)}\n"
        f"</properties>\n</page>"
    )
    out = {"title": title, "url": url, "text": text}
    RAW.mkdir(exist_ok=True)
    (RAW / f"{page_id}.json").write_text(json.dumps(out, ensure_ascii=False, indent=2), encoding="utf-8")


def main() -> None:
    for pid, title, props in QA_PAGES:
        save_page(pid, title, props)
    print(f"Wrote {len(QA_PAGES)} QA pages to {RAW}")


if __name__ == "__main__":
    main()
